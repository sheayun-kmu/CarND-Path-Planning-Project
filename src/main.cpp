#include <uWS/uWS.h>
#include <fstream>
#include <iostream>
#include <string>
#include <vector>
#include "Eigen-3.3/Eigen/Core"
#include "Eigen-3.3/Eigen/QR"
#include "helpers.h"
#include "json.hpp"
#include "planner.h"

// for convenience
using nlohmann::json;
using std::string;
using std::vector;

int main() {
  uWS::Hub h;

  // Load up map values for waypoint's x,y,s and d normalized normal vectors
  vector<double> map_waypoints_x;
  vector<double> map_waypoints_y;
  vector<double> map_waypoints_s;
  vector<double> map_waypoints_dx;
  vector<double> map_waypoints_dy;

  // Waypoint map to read from
  string map_file_ = "../data/highway_map.csv";
  // The max s value before wrapping around the track back to 0
  double max_s = 6945.554;

  std::ifstream in_map_(map_file_.c_str(), std::ifstream::in);

  string line;
  while (getline(in_map_, line)) {
    std::istringstream iss(line);
    double x;
    double y;
    float s;
    float d_x;
    float d_y;
    iss >> x;
    iss >> y;
    iss >> s;
    iss >> d_x;
    iss >> d_y;
    map_waypoints_x.push_back(x);
    map_waypoints_y.push_back(y);
    map_waypoints_s.push_back(s);
    map_waypoints_dx.push_back(d_x);
    map_waypoints_dy.push_back(d_y);
  }

  double current_vel = 0.0;
  int current_lane = 1;
  Behaviour bp = Behaviour(
    current_lane, map_waypoints_s, map_waypoints_x, map_waypoints_y
  );

  h.onMessage([&map_waypoints_x, &map_waypoints_y, &map_waypoints_s,
               &map_waypoints_dx, &map_waypoints_dy, &current_vel, &bp]
              (uWS::WebSocket<uWS::SERVER> ws, char *data, size_t length,
               uWS::OpCode opCode) {
    // "42" at the start of the message means there's a websocket message event.
    // The 4 signifies a websocket message
    // The 2 signifies a websocket event
    if (length && length > 2 && data[0] == '4' && data[1] == '2') {

      auto s = hasData(data);

      if (s != "") {
        auto j = json::parse(s);

        string event = j[0].get<string>();

        if (event == "telemetry") {
          // j[1] is the data JSON object

          // Main car's localization Data
          double car_x = j[1]["x"];
          double car_y = j[1]["y"];
          double car_s = j[1]["s"];
          double car_d = j[1]["d"];
          double car_yaw = j[1]["yaw"];
          double car_speed = j[1]["speed"];

          // Previous path data given to the Planner
          auto previous_path_x = j[1]["previous_path_x"];
          auto previous_path_y = j[1]["previous_path_y"];
          // Previous path's end s and d values
          double end_path_s = j[1]["end_path_s"];
          double end_path_d = j[1]["end_path_d"];

          // Sensor Fusion Data, a list of all other cars on the same side
          //   of the road.
          auto sensor_fusion = j[1]["sensor_fusion"];

          json msgJson;

          /**
           * TODO: define a path made up of (x,y) points that the car will visit
           *   sequentially every .02 seconds
           */

          vector<double> px = previous_path_x.get<vector<double>>();
          vector<double> py = previous_path_y.get<vector<double>>();
          vector<vector<double>> sf;
          sf = sensor_fusion.get<vector<vector<double>>>();

          double target_vel = Config::max_speed;

          // determine the next behaviour to take
          // (based on the situation given by sensor fusion)
          int prev_size = previous_path_x.size();
          bp.set_ego_status(car_s, car_d, car_speed,
                            target_vel, end_path_s, end_path_d,
                            sf, prev_size);
          bp.determine_next();
          // determine which lane to follow (based on bahaviour)
          vector<vector<double>> wp = bp.get_waypoints();

          // get a set of waypoints to follow given the lane to follow
          // vector<vector<double>> wp = nb.get_waypoints(target_lane);
          // initialize a path for ego vehicle
          // (containing only the waypoints defined by the previous path)
          Path p = Path(px, py, wp, car_x, car_y, car_yaw);
          // plan motion (vel and acc in the target lane)
          int target_lane = bp.get_target_lane();
          // now let agent_ahead point to the vehicle we want to
          // keep speed at
          Agent curr_ahead;
          Agent tgt_ahead;
          Agent tracking;
          tgt_ahead = bp.check_ahead(target_lane);
          if (bp.get_current_behaviour() == Behaviour::lane_change_left ||
              bp.get_current_behaviour() == Behaviour::lane_change_right) {
            curr_ahead = bp.check_ahead(bp.get_current_lane());
          }
          if (curr_ahead.lane < 0) {
            tracking = tgt_ahead;
          } else if (tgt_ahead.lane < 0) {
            tracking = curr_ahead;
          } else if (curr_ahead.s < tgt_ahead.s) {
            tracking = curr_ahead;
          } else {
            tracking = tgt_ahead;
          }
          current_vel = p.motion_plan(end_path_s, tracking,
                                      current_vel, target_vel);
          vector<double> next_x_vals = p.get_path_x();
          vector<double> next_y_vals = p.get_path_y();

          msgJson["next_x"] = next_x_vals;
          msgJson["next_y"] = next_y_vals;

          auto msg = "42[\"control\","+ msgJson.dump()+"]";

          ws.send(msg.data(), msg.length(), uWS::OpCode::TEXT);
        }  // end "telemetry" if
      } else {
        // Manual driving
        std::string msg = "42[\"manual\",{}]";
        ws.send(msg.data(), msg.length(), uWS::OpCode::TEXT);
      }
    }  // end websocket if
  }); // end h.onMessage

  h.onConnection([&h](uWS::WebSocket<uWS::SERVER> ws, uWS::HttpRequest req) {
    std::cout << "Connected!!!" << std::endl;
  });

  h.onDisconnection([&h](uWS::WebSocket<uWS::SERVER> ws, int code,
                         char *message, size_t length) {
    ws.close();
    std::cout << "Disconnected" << std::endl;
  });

  int port = 4567;
  if (h.listen(port)) {
    std::cout << "Listening to port " << port << std::endl;
  } else {
    std::cerr << "Failed to listen to port" << std::endl;
    return -1;
  }

  h.run();
}